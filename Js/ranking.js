var pontuacaoAtual = 0




function pergunta1(){
    let resposta1 = document.getElementById("quest-1_alternativa-1").checked
    if (resposta1 == true) 
    {
        document.getElementById("span1").textContent = "Você acertou"
        pontuacaoAtual += 20
        document.getElementById("record").textContent = pontuacaoAtual

        console.log(`Pontuação atual ${pontuacaoAtual}`)
    }
    else
    {
        document.getElementById("span1").textContent = "Você errou"
        pontuacaoAtual -= 15
        document.getElementById("record").textContent = pontuacaoAtual

        console.log(`Pontuação atual ${pontuacaoAtual}`)
    }
}

